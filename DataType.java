public class DataType {
    public static void main (String args[])
    {
        //integrals 
        byte a=1;
        short b= 10;
        int c = 536871066;
        long d = 53687106676483930l;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

        //floating
        float e = 12.123456f;
        double f = 12.1234567898765;
        System.out.println(e);
        System.out.println(f);

        //character
        char g = 'a'; 
        char h = 65; //A
        System.out.println(g);
        System.out.println(h);
        //here we can use ASCII value or UNICODE value (unicode are code which are used to write symbol which are not in ascii table)

        //boolean
        boolean i = true;
        System.out.println(i);
    }
}
